# define 4 points for ROI
def selectROI(event, x, y, flags, param):
    global roiPts

    if event == cv2.EVENT_LBUTTONDOWN and len(roiPts) < 4:
        roiPts.append((x, y))
        print(x, y)
        print("select ROI")

